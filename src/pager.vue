<script>
  import FIcon from './icon'
  export default {
    name: 'FeelPager',
    components: { FIcon },
    props: {
      totalPage: {
        type: Number,
        required: true
      },
      currentPage: {
        type: Number,
        required: true
      },
      hideIfOnePage: {
        type: Boolean,
        default: true
      }
    },
    data() {
      let pages = unique(
        [
          1,
          this.totalPage,
          this.currentPage,
          this.currentPage - 1,
          this.currentPage - 2,
          this.currentPage + 1,
          this.currentPage + 2
        ]
          .filter(n => n >= 1 && n <= this.totalPage)
          .sort((a, b) => a - b)
      ).reduce((prev, current, index, array) => {
        prev.push(current)
        array[index + 1] !== undefined && array[index + 1] - array[index] > 1 && prev.push('...')
        return prev
      }, [])

      return {
        pages
      }
    }
  }

  function unique(array) {
    // return [...new Set(array)]
    // array = [1 1 2 3 4 5 20]
    const object = {}
    array.map(number => {
      object[number] = true
    })
    return Object.keys(object).map(s => parseInt(s, 10))
  }
</script>

